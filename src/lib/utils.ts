export function cn(...inputs: (string | undefined | null | false)[]) {
  return inputs
    .flat()
    .filter(Boolean)
    .join(" ");
}

export function formatCount(count: number): string {
  if (count >= 1000) {
    return `${(count / 1000).toFixed(1)}k`;
  }
  return count.toString();
}

export function slugify(text: string): string {
  return text
    .toLowerCase()
    .replace(/\s+/g, "-")
    .replace(/[^\w-]+/g, "");
}
